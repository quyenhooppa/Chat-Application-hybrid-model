    private void checkAccount() {
        // does not give enough infomation
        if (userName.getText().isEmpty() || password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                "Please enter BOTH your name and pass.");
        } else {
            String name = userName.getText().trim();
            String pass = password.getText().trim();

            // check format of name
            if (!name.matches("^[a-zA-z0-9]+$") || name.matches("^[0-9].*$")) {
                userName.setText("");
                password.setText("");
                        
                JOptionPane.showMessageDialog(null,
                    "Login Failed. Please try again.");
                
            } else {
                
                User newUser = new User(name, pass);
                newUser.requestToServer("login");
                newUser.getRequestServer().setLoginUI(this);
                newUser.getRequestServer().start();
            }
        }
    }
    
